export function getKoreanHolidaysForMonth(
  year: number,
  month: number,
): Map<string, KoreanHoliday[]> {
  const prefix = `${year}-${String(month).padStart(2, "0")}`;
  const byDate = new Map<string, KoreanHoliday[]>();
  for (const h of HOLIDAYS_BY_YEAR[year] ?? []) {
    if (!h.date.startsWith(prefix)) continue;
    const list = byDate.get(h.date) ?? [];
    list.push(h);
    byDate.set(h.date, list);
  }
  return byDate;
}
